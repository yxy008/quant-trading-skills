def get_stock_price(symbol):
    """获取股票实时价格"""
    df = get_stock_kline(symbol, days=30)

    if df is not None and not df.empty and len(df) >= 2:
        latest = df.iloc[-1]
        prev = df.iloc[-2]
        change = (float(latest['收盘']) - float(prev['收盘'])) / float(prev['收盘']) * 100
        return {
            "最新价": round(float(latest['收盘']), 2),
            "涨跌幅": round(change, 2)
        }

    return {
        "最新价": 0,
        "涨跌幅": 0
    }


def generate_pool(sectors=None, count_per_sector=5, all_stocks=False):
    """生成股票池，默认展示全部板块"""
    if sectors is None:
        sectors = list(PREMIUM_STOCKS.keys())
            
    stock_list = []
    
    for sector in sectors:
        if sector not in PREMIUM_STOCKS:
            continue
            
        stocks = PREMIUM_STOCKS[sector][:count_per_sector]
        
        for s in stocks:
            price_data = get_stock_price(s['代码'])
            stock_info = {
                "代码": s['代码'],
                "名称": s['名称'],
                "板块": sector,
                "标签": s['标签'],
                "最新价": price_data['最新价'],
                "涨跌幅": price_data['涨跌幅']
            }
            stock_list.append(stock_info)
            
    # 按涨跌幅排序（优先正涨幅）
    stock_list.sort(key=lambda x: -x['涨跌幅'])
    
    return {
        "日期": datetime.now().strftime('%Y-%m-%d'),
        "候选池数量": len(stock_list),
        "覆盖板块": sectors,
        "股票列表": stock_list
    }


def main():
    parser = argparse.ArgumentParser(description='候选股票池生成工具')
    parser.add_argument('action', choices=['generate'], help='操作类型: generate（生成候选池）')
    parser.add_argument('--sectors', help='板块列表，逗号分隔')
    parser.add_argument('--count', type=int, default=3, help='每个板块推荐数量')
    parser.add_argument('--all', action='store_true', help='是否包含所有板块')
    
    args = parser.parse_args()
    
    try:
        if args.action == 'generate':
            sectors = None
            if args.sectors:
                sectors = [s.strip() for s in args.sectors.split(',')]
            data = generate_pool(sectors=sectors, count_per_sector=args.count, all_stocks=args.all)
            print(json.dumps(data, ensure_ascii=False, indent=2))
            
    except Exception as e:
        print(json.dumps({"error": str(e)}, ensure_ascii=False, indent=2))
        import traceback
        traceback.print_exc()
        sys.exit(1)


if __name__ == '__main__':
    main()
